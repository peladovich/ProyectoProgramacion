    private void btnGuardarInasistenciaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarInasistenciaActionPerformed
        // TODO add your handling code here:
       try {
        // ðŸ”¹ Obtener la fecha del spinner
        java.util.Date fechaD = (java.util.Date) spnDesde.getValue();
        java.util.Date fechaH = (java.util.Date) spnHasta.getValue();

        // ðŸ”¹ Crear la inasistencia (usa LocalDate si tu clase lo requiere)
        Inasistencia i = new Inasistencia(
            txtCedulaDocente.getText().trim(),
            new java.sql.Date(fechaD.getTime()).toLocalDate(),
            new java.sql.Date(fechaH.getTime()).toLocalDate()
        );

        // ðŸ”¹ Guardar en base de datos
        new FachadoInasistencia().registrarInasistencia(i);
        JOptionPane.showMessageDialog(this, "Inasistencia registrada correctamente.");
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
    }
    }//GEN-LAST:event_btnGuardarInasistenciaActionPerformed

